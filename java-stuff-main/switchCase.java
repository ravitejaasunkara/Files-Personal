public class switchCase {
    public static void main(String[] args){
        int x = 10;
        switch(x){
            case 1:
                System.out.println("hi");
                break;
            case 2:
                System.out.println("hello");
                break;
            case 10:
                System.out.println("how are you!!");
                break;
            default:
                System.out.println("bye");
        }
        //int[] y = {1,2,3,4};
    }
}
